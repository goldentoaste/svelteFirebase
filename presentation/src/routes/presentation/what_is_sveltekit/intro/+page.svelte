<h1 class="title">SvelteKit, Briefly</h1>

<p>
    We have covered more than enough to build a simple todo list app. However
    Svelte is just a tool to build UI components, we want to build an actual
    website! SvelteKit is designed to be the companion "meta-framework" to do
    things like routing and SSR, kinda like NextJS to React.
    <br /> <br />
    It is certainly possible to use Svelte without SvelteKit,
    <a target="_blank" href="https://docs.astro.build/en/concepts/why-astro/">Astro</a> is a popular
    option.
</p>

<p>
    SvelteKit covers most aspects a full stack framework should, such as the
    following. We will only use the routing part to keep things simple.
</p>
<ul>
    <li>Routing</li>
    <li>Server-side rendering (SSR)</li>
    <li>Data fetching / db connection</li>
    <li>Servering web page and API</li>
    <li>Service worker</li>
    <li>Typescript integration</li>
    <li>Library optimization and bundling</li>
    <li>And <em>so</em> much more...</li>
</ul>
